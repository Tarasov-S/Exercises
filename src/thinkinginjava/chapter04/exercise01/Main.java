package thinkinginjava.chapter04.exercise01;

/*
 * Chapter 4. Exercise 1. Difficult - 1/10.
 *
 * Write a program that prints values from 1 to 100.
 */

public class Main {

    public static void main(String[] args) {

        for (int i = 1; i <= 100; i++) {
            System.out.println(i);
        }
    }
}
/* Out:
 *
 * 1
 * 2
 * 3
 * 4
 *....
 *....
 *....
 * 96
 * 97
 * 98
 * 99
 * 100
 */
